 <h4>Workshops</h4>
      
	
 <p>The 12th ACM Conference on Embedded Networked Sensor Systems (SenSys 2014) is
  a highly selective, single-track forum for research on systems issues of
  networked sensing and actuation, broadly defined. Systems of smart sensors
  and actuators will revolutionize a wide array of application areas by
  providing an unprecedented density and fidelity of instrumentation. They also
  present systems challenges because of resource constraints, uncertainty,
  irregularity, mobility, and scale. This conference provides an ideal venue to
  address research challenges facing the design, development, deployment, use,
  and fundamental limits of these systems. Sensing and actuation systems
  require contributions from many fields, from wireless communication and
  networking, embedded systems and hardware, energy harvesting and management,
  distributed systems and algorithms, data management, and applications, so we
  welcome cross-disciplinary work.</p>

  <p>Workshops will be held on Thursday, Nov 6, 2014.</p>


