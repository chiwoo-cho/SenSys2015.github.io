          <h4>Become a supporter</h4>
      
      <p>

SenSys welcomes financial support from companies and organizations. Supporting SenSys is a great opportunity to increase visibility and product awareness within networked sensing system communities. </p>

<p>
Supporters will be acknowledged at the conference opening remark. Company names and logos will appear on SenSys 2014 website and proceedings. In addition, each supporter will receive a table at the demo/poster session, if they wish, to showcase their product to the sensor system research community. The minimum support is $1,000. Every $5,000 support will receive one free conference registration.</p>

<p>
Supporters may direct their fund to specific SenSys 2012 awards or events, such as Best Paper Award(s), Best Demo Award(s), Best Poster Award(s), Student Travel Award(s), N2Women, and conference reception. Other demands can be discussed on a case-by-case basis.
</p>

