          <h4>Welcome to ACM SenSys 2014!</h4>
          <p>
        The 12th ACM Conference on Embedded Networked Sensor Systems is a highly selective, single-track forum for the presentation of research results on systems issues in the area of embedded, networked sensors. Distributed systems based on networked sensors and actuators with embedded computation capabilities allow for an instrumentation of the physical world at an unprecedented scale and density, thus enabling a new generation of monitoring and control applications. SenSys provides a cross-disciplinary venue for researchers addressing the rich space of networked sensor system design issues to interact, present and exchange research results, and demonstrate their work in a hands-on research exhibition. We seek technical papers describing original, previously unpublished research results.
 
