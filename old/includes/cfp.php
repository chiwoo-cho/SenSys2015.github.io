          <h4>Call for Papers</h4>
          <p>
			  Sensors have become an essential part of computing systems and applications. Computing today is increasingly characterized by ubiquitous, information-rich sensors that produce massive quantities of data about the physical world. This new era of computing is driving important new systems issues, and requires new system-level approaches and design principles.
</p>
<p>
	The ACM Conference on Embedded Networked Sensor Systems (SenSys 2014) is a computer systems conference focused on the architecture, design, implementation,
and performance of sensor systems. ACM SenSys brings together academic, industry, and government professionals to a single-track, highly selective
forum on sensor network design, implementation, and application. It is the premier forum to discuss systems issues that arise specifically due to sensing.
SenSys takes a broad view on the areas of computing that are relevant to the future of sensor systems, and topics of interest include but are not limited to the following:</p>
  
          <h4> Topics of Interest</h4>
          
          <ul>
<li>Compelling challenge papers grounded in technology trends</li>
<li>Applications and deployment experiences</li>
<li>Knowledge discovery from sensor data</li>
<li>Emerging sensor systems, such as Kinect, LIDAR, and cameras</li>
<li>RFID computation, communications, storage, and networking</li>
<li>Mobile and wearable sensing</li>
<li>Ubiquitous and pervasive sensing</li>
<li>Internet of Things, Cyber-Physical Systems, and Sensor Swarms</li>
<li>Software for sensor systems</li>
<li>Communication and networking for sensor systems</li>
<li>Sensor context such as time and location estimation</li>
<li>Energy harvesting and management for long-term sensor operation</li>
<li>Storage, retrieval, processing, and management of sensor data</li>
<li>New sensor technology and hardware designs</li>
<li>Fault-tolerance and reliability of sensor systems</li>
<li>Sensor data quality, integrity, and trustworthiness</li>
<li>Security and privacy of sensor systems</li>
</ul>

<p>
	We invite technical papers describing original ideas, ground-breaking results, and/or real-world experiences involving innovative sensor systems. Successful submissions will explain why the topic is relevant to a vision of the future of sensing systems. Submissions will be judged on originality, significance, clarity, relevance, and correctness. In addition to citing relevant, published work, authors must cite and relate their submissions to relevant prior publications of the their own.
</p>

<h4>Submission Guidelines</h4>

<p>
Submissions must be full papers, at most 14 single-spaced 8.5" x 11" pages, including figures, tables, and references, two-column format, using 10-point type on 11-point (tight single-spaced) leading, with a maximum text block of 7" wide x 9" deep with an intercolumn spacing of .25". Authors must make a good faith effort to anonymize their submissions. Papers that do not meet the size, formatting, and anonymization requirements will not be reviewed. Accepted submissions will be available on the ACM digital library at least one week before the conference.</p>
        
